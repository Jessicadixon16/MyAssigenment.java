public class birthdayName {
    public static String birthdayName(String name) {
        return ("Happy Birthday" + name + "!");
    }
    public static void main (String [] args) {

            System.out.println(birthdayName("Juan")); // happy birthday Juan!
        System.out.println(birthdayName("Cindy")); // happy birthday Cindy!
        System.out.println(birthdayName("John")); // happy birthday John!
        }
    }

